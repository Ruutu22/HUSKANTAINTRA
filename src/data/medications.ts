// Hae lääkkeet kategorian mukaan
export function getMedicationsByCategory(categoryId: string): MedicationData[] {
  return medications.filter(m => m.category === categoryId);
}

// Hae lääke nimellä
export function getMedicationByName(name: string): MedicationData | undefined {
  return medications.find(m => m.name.toLowerCase() === name.toLowerCase());
}

// Hae lääkkeet hakusanalla
export function searchMedications(query: string): MedicationData[] {
  const lowerQuery = query.toLowerCase();
  return medications.filter(m => 
    m.name.toLowerCase().includes(lowerQuery) ||
    m.activeIngredient.toLowerCase().includes(lowerQuery) ||
    m.uses.some(u => u.toLowerCase().includes(lowerQuery))
  );
}
